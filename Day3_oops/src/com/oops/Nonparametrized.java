package com.oops;

public class Nonparametrized
{
	String str;
	int a;
	Nonparametrized()
	{
		str="sanjay";
		a=11;
		
	}
	void display()
	{
		System.out.println(str+" "+a);
	}
	

	public static void main(String[] args)
	{
		Nonparametrized c=new Nonparametrized ();
		c.display();
		
	}

}


	

